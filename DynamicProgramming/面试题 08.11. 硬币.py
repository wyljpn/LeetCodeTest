# i : 25, 26, 27, 28, 29, 30
# f[25]=f[0] f[26]=f[1] f[27]=f[2] f[28]=f[3] f[29]=f[4] f[30]=f[5]

# coin : 10
# i : 10, 11, 12, 13, 14, 15, ... 30
